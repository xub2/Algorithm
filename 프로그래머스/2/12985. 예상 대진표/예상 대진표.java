//                          1-2 싸움에선 승자가 1이 된다.
// 3-4 싸움에선 승자가 2가 된다  
//                                                      1-2 싸움에서 승자가 1이 된다
// 5-6 싸움에선 승자가 3이 된다
//                          3-4 싸움에선 승자가 2이 된다.
